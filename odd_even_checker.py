# List of 15 numbers
numbers = [3, 8, 15, 22, 7, 10, 33, 40, 13, 28, 5, 16, 21, 12, 30]

# Loop through each number and determine if it's even or odd
for number in numbers:
    if number % 2 == 0:
        print(str(number) + " is even")
    else:
        print(str(number) + " is odd")

        
